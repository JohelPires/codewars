function accum(s) {
	// your code
    function addLetters(l,i) {
        let str = ''
        if (i>0){
            for (j=0; j<=i-1; j++){
                str += l.toLowerCase()
            }
            return l+str
        }
        return l
        
    }

    
    return s.toUpperCase().split('').map(addLetters).join('-')
}

